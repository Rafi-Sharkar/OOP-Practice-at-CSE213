package RS_tut;

/*
Do while loop

    simplie__
    1. Execute the statement
    2. Update the condition's variable.
    3. check condition
 */

public class RS_18_DoWhileLoop {
    public static void main(String[] args) {
        int b = 5;
        do {
            System.out.println(b);
            b++;
        }while (b<5);
    }
}
